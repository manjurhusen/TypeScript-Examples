//Example of for loop
console.log("Output:"); 
for (let i = 0; i < 3; i++) {
    console.log(i);
}
/*
 Output: 
 0 
 1 
 2  
*/

//string array
let users:string[] = ["Jay", "Vijay", "Ashish"];
console.log("Output:");
for (let i = 0; i < users.length; i++) {
    console.log(`name=${users[i]}`);
}
/*
 Output: 
 name=Jay
 name=Vijay 
 name=Ashish 
*/